            Fundado el 03 de noviembre de 1962. Los fines fundamentales del Consejo son: 
            <ul class="list-consejo">
                <li>La defensa de los intereses profesionales de sus miembros;</li>
                <li>Velar por el mantenimiento de la ética profesional;</li>
                <li>Promover la mayor ilustración en el ejercicio profesional de la abogacía. </li>
            </ul>
            En pos de la consecución de sus fines, y en su permanente actividad como entidad gremial, 
            a lo largo de éstos años ha realizado numerosas actividades académicas en el ámbito local 
            y nacional con participación activa de sus socios en Congresos, Conferencias Nacionales, 
            reuniones nacionales de carácter científico y jornadas de capacitación. 
            <br>
            El CONSEJO PROFESIONAL DE ABOGADOS Y PROCURADORES busca la unidad de todos los profesionales del 
            derecho mediante el Sistema Colegial Público, lo que va a significar garantía para el justiciable 
            de que el profesional que lo atienda sea idóneo, tanto moral como técnicamente, ya que, con el control 
            de la matrícula  y los tribunales de disciplina, se garantiza la mejor defensa de los derechos de quienes  representa.
